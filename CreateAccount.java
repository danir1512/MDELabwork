    private void CreateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateButtonActionPerformed
        name = NameTextField.getText();
        password = new String(PasswordField.getPassword());
        email = EmailTextField.getText();
        address = AddressTextField.getText();
        NIF = NIFTextField.getText();
        username = UsernameTextField.getText();
        if (name.equals("") || password.equals("") || email.equals("") || address.equals("") || NIF.equals("") || username.equals("")) {
            JOptionPane.showMessageDialog(null, "Campos vazios" + "\n" + "Por favor preencha os campos em falta", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                
                /*String str = "insert into Client "
                        +" (ClientID, Name, Password, Email, Adress, NIF, Username)"
                        + " values ('2', 'Juan', '123456', 'juan@gmail.com', 'malamadre', '123456', 'x_juan_x')";
                //" + "2" + ", " + name + "," + password + ", " + email + ", " + address + ", " + NIF + ", " + username + "
                //stmt = conn.createStatement();
                ResultSet rset = stmt.executeQuery(str);
                //stmt.executeUpdate(str);*/
                String n_rows = ("Select COUNT(*) FROM Client");
                String str = ("insert into Client(CLIENTID, NAME, EMAIL, ADRESS, NIF, PASSWORD, USERNAME)values(?, ?, ?, ?, ?, ?, ?)");
                PreparedStatement countRows = conn.prepareStatement(n_rows);
                ResultSet rset = countRows.executeQuery();
                PreparedStatement preparedStmt = conn.prepareStatement(str);
                rset.next();
                preparedStmt.setInt(1,rset.getInt("COUNT(*)")+1);
                preparedStmt.setString(2, name);
                preparedStmt.setString(3, email);
                preparedStmt.setString(4, address);
                preparedStmt.setString(5, NIF);
                preparedStmt.setString(6, password);
                preparedStmt.setString(7, username);
                preparedStmt.executeUpdate();
                //conn.commit();
                //stmt = conn.createStatement();
                //stmt.executeUpdate("insert into Client(CLIENTID, NAME, EMAIL, ADRESS, NIF, PASSWORD, USERNAME)values(?, ?, ?, ?, ?, ?, ?)");
                name = null;
                password = null;
                email = null;
                address = null;
                NIF = null;
                username = null;
                
            } catch (SQLException ex) {
                Logger.getLogger(CreateAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CreateButtonActionPerformed

